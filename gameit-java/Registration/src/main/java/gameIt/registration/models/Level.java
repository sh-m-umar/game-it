package gameIt.registration.models;

public class Level {
	
	private int id;
	private String levelName;
	
	public Level() {}
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getLevelName() {
		return levelName;
	}
	public void setLevelName(String levelName) {
		this.levelName = levelName;
	} 
	
	
	
	
}
